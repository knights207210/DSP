n=0:8191;
a=0.1;
f=0.0625;
x=exp(-a*n).*sin(2*pi*f*n).*(n>=0&n<=15)+0*(n>15);
tic;
y=myfft(x);
toc;
tic;
y1=fft(x);
toc;
tic;
y2=dft(x,8192);
toc;
subplot(3,1,3);stem(abs(y2));
title('DFT');
