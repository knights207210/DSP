n=0:63;      
A=444.128;      
a=50*sqrt(2.0)*pi;   
T=1/200;     
w0=50*sqrt(2.0)*pi;    
x=A*exp(-a*n*T).*sin(w0*n*T);
tic;
y=myfft(x);
MagY=abs(y);subplot(3,2,2);stem(MagY);
toc;
tic;
y1=fft(x);
MagY1=abs(y1);
subplot(3,2,4);stem(MagY1);
toc;
tic;
y2=dft(x,64);
toc;
MagY2=abs(y2);
subplot(3,2,6);stem(MagY2);
subplot(3,2,5);stem(y2);
title('DFT');
subplot(3,2,1)
stem(y);
title('myfft');
subplot(3,2,3);
stem(y1);
title('matlab-fft');

