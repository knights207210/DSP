%首先先产生信号x[n]
n=0:50;%序列长为51
A=444.128;
a=50*sqrt(2.0)*pi;
T=1/1600;%采样率,1000,300,200
w0=50*sqrt(2.0)*pi;
x=A*exp(-a*n*T).*sin(w0*n*T);
close all;%清除已经绘制的x[n]图形
subplot(3,3,1);
stem(x);
title('T=1000');
%绘制信号的幅度谱和相位谱
k=-25:25;%对信号做DTFT变换，DTFT变换之后是连续且周期的的，周期为2pi，需将其离散化。
%在区间[-2pi,2pi]取51个点，则W=(2pi/25)*k
X=x*(exp(-j'*pi/12.5)).^(n'*k);
magX=abs(X);
subplot(3,3,2);
stem(magX);
angX=angle(X);
subplot(3,3,3);
stem(angX);

